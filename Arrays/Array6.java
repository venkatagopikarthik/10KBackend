package Arrays;

public class Array6 {
    public static void main(String[] args) {
        int[] intarr=new int[5];
        intarr[0] =5;
        intarr[1] =15;
        intarr[2] =25;
        intarr[3] =35;
        intarr[4] =45;
        for(int n:intarr){
            System.out.println(n);
        }
    }
    
}
