package Arrays;

public class Array8 {
    public static void main(String[] args) {
        float[] flarr=new float[4];
        flarr[0]=1.1f;
        flarr[1]=2.2f;
        flarr[2]=3.3f;
        flarr[3]=4.4f;
        for(float f:flarr){
            System.out.println(f);
        }

    }
    
}
